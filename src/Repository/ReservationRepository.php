    public function findReservationByIdReservation($idReservation)
    {
        return $this->createQueryBuilder('res')
            ->where('res.idReservation LIKE :id_reservation')
            ->setParameter('id_reservation', '%'.$idReservation.'%')
            ->getQuery()
            ->getResult();
    }

    public function SortEvent()
    {
        return $this->createQueryBuilder('e')
            ->orderBy('e.date', 'DESC')
